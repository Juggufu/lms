nums = float(input())
nums_2 = float(input())
num = input()
if num == "*":
        print(nums * nums_2)
elif num == "-":
        print(nums - nums_2)
elif num == "+":
        print(nums + nums_2)
elif num == "/":
        print(nums / nums_2)
elif nums / nums_2 and nums_2 == "0":
        print("888888")
else:
        print("888888")